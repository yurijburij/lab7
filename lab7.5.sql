SELECT Послуги.[Назва послуги], Послуги.[Код послуги], [Запис санаторію].[Година запису], 
[Запис санаторію].[Код пацієнта], [Запис санаторію].[Код запису], [Запис санаторію].[Код послуги]
FROM Послуги INNER JOIN [Запис санаторію]
ON Послуги.[Код послуги] = [Запис санаторію].[Код запису]
WHERE Послуги.[Код послуги] IN (SELECT [Запис санаторію].[Код послуги] FROM [Запис санаторію] 
WHERE [Запис санаторію].[Година запису] = '17:00:00')
ORDER BY Послуги.[Назва послуги];