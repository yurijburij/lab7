SELECT Пацієнт.[Код пацієнта], Пацієнт.[Ім*я пацієнта], Пацієнт.[Прізвище пацієнта], Послуги.[Назва послуги],
Послуги.[Ціна послуги]
FROM Пацієнт INNER JOIN Послуги
ON Пацієнт.[Код пацієнта]=Послуги.[Код послуги]
AND Послуги.[Код послуги]=Пацієнт.[Код пацієнта]
WHERE Пацієнт.[Прізвище пацієнта]='Бурій'